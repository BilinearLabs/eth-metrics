func CreateMockKeysFile(customKeysFile string, content string) {
	f, err := os.Create(customKeysFile)

	if err != nil {
		log.Fatal(err)
	}

	_, err = f.WriteString(content)

	if err != nil {
		log.Fatal(err)
	}
	f.Close()
}

func TestReadCustomValidator(t *testing.T) {
	customKeysFile := "test_file.txt"
	chaindFile := "chaind_file.txt"
	bqueryFile := "bigquery_file.txt"

	// Test all 3 formats that ReadCustomValidatorsFile supports
	CreateMockKeysFile(customKeysFile, rawKeys)
	defer os.Remove(customKeysFile)

	CreateMockKeysFile(chaindFile, rawKeysChaind)
	defer os.Remove(chaindFile)

	CreateMockKeysFile(bqueryFile, rawKeysBigQuery)
	defer os.Remove(bqueryFile)

	keys, err := ReadCustomValidatorsFile(customKeysFile)
	require.NoError(t, err)
	require.Equal(t, 4, len(keys))
	for i, key := range keys {
		require.Equal(t, expectedKeys[i], key)
	}

	keys, err = ReadCustomValidatorsFile(chaindFile)
	require.NoError(t, err)
	require.Equal(t, 2, len(keys))
	for i, key := range keys {
		require.Equal(t, expectedChaind[i], key)
	}

	keys, err = ReadCustomValidatorsFile(bqueryFile)
	require.NoError(t, err)
	require.Equal(t, 2, len(keys))
	for i, key := range keys {
		require.Equal(t, expectedBigQuery[i], key)
	}
}

func TestReadEthstaValidatorsFile(t *testing.T) {
	customKeysFile := "ethsta_example.csv"
	CreateMockKeysFile(customKeysFile, rawEthstaCsv)
	defer os.Remove(customKeysFile)

	keys, err := ReadEthstaValidatorsFile(customKeysFile)
	require.NoError(t, err)
	require.Equal(t, 3, len(keys))

	for i, key := range keys {
		require.Equal(t, expectedKeysEthsta[i], key)
	}
}
